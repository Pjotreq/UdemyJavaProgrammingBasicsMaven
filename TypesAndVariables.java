public class TypesAndVariables {
    public static void main (String[] args) {
        int myVariable;
        myVariable = 5;

        System.out.println("Value of myVariable is " + myVariable);
        myVariable = 10;
        System.out.println("Value of myVariable is " + myVariable);
        myVariable = myVariable + 1 + 3;
        System.out.println("Value of myVariable is " + myVariable );
    }
}
